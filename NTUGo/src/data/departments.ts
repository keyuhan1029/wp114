// 取得所有系所的扁平列表
export const getAllDepartments = (): string[] => {
  return NTU_DEPARTMENTS.flatMap(category => category.departments);
};

// 根據系所名稱取得所屬學院
export const getCollegeByDepartment = (department: string): string | null => {
  for (const category of NTU_DEPARTMENTS) {
    if (category.departments.includes(department)) {
      return category.name;
    }
  }
  return null;
};

